package com.sist;

public class Ex02 {

	public static void main(String[] args) {
		System.out.println("프로그램 시작");
		System.out.print("java\t"); // 다음 출력문이 오른쪽에 출력된다.
		System.out.print("programming\n");
		System.out.print("프로그램 종료");
		
		/*
		 * 제어문자
		 * \n : 줄바꿈을 제공하는 제어문자.
		 * \t : 탭만큼의 칸을 띄어주는 제어문자.
		 * ctrl + shift + f : 들여쓰기 자동 정렬
		 */
	}

}
